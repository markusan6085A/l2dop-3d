/**
 * Генерує server/src/data/l2dopNpcIdByName.generated.ts з *Mobs.generated.ts (text-rpg).
 * Запуск: node scripts/gen-l2dop-npc-map.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
/** server → l2dop-3d → Desktop/text-rpg */
const trpg = path.join(root, '..', '..', 'text-rpg', 'src', 'data', 'world', 'l2dop');

const FILES = [
  'giranMobs.generated.ts',
  'dionMobs.generated.ts',
  'floranVillageMobs.generated.ts',
  'gludinVillageMobs.generated.ts',
  'heineMobs.generated.ts',
  'huntersVillageMobs.generated.ts',
  'runeMobs.generated.ts',
  'schuttgartMobs.generated.ts',
  'goddardMobs.generated.ts',
  'goddardHighMobs.generated.ts',
];

function extractPairs(content) {
  const pairs = [];
  const re = /id:\s*"l2dop_(\d+)"[\s\S]*?name:\s*"((?:[^"\\]|\\.)*)"/g;
  let m;
  while ((m = re.exec(content)) !== null) {
    pairs.push({ npcId: parseInt(m[1], 10), name: JSON.parse('"' + m[2].replace(/\\"/g, '"') + '"') });
  }
  return pairs;
}

function main() {
  const byName = Object.create(null);
  for (const f of FILES) {
    const p = path.join(trpg, f);
    if (!fs.existsSync(p)) {
      console.warn('skip (немає файлу):', p);
      continue;
    }
    const content = fs.readFileSync(p, 'utf8');
    for (const { npcId, name } of extractPairs(content)) {
      if (byName[name] == null) byName[name] = npcId;
    }
  }
  const keys = Object.keys(byName).sort();
  const lines = keys.map((k) => {
    const safe = JSON.stringify(k);
    return `  ${safe}: ${byName[k]},`;
  });
  const out = `/* AUTO-GENERATED by scripts/gen-l2dop-npc-map.mjs — не редагувати вручну */
export const L2DOP_NPC_ID_BY_NAME: Record<string, number> = {
${lines.join('\n')}
};
`;
  const outPath = path.join(root, 'src', 'data', 'l2dopNpcIdByName.generated.ts');
  fs.writeFileSync(outPath, out, 'utf8');
  console.log('written', outPath, 'entries', keys.length);
}

main();
