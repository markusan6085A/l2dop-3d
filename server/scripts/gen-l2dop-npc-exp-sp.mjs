/**
 * Читає lineage.sql (l2dop) — INSERT INTO `npc` — і генерує l2dopNpcExpSp.generated.ts
 * Шлях: ../l2dop/lineage.sql відносно каталогу server/ (тобто Desktop/l2dop при стандартному розкладі).
 */
import { createReadStream } from 'fs';
import { createWriteStream } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';
import { createInterface } from 'readline';

const __dirname = dirname(fileURLToPath(import.meta.url));
const serverDir = join(__dirname, '..');
const defaultSql = join(serverDir, '..', '..', 'l2dop', 'lineage.sql');
const outPath = join(serverDir, 'src', 'data', 'l2dopNpcExpSp.generated.ts');

const sqlPath = process.env.L2DOP_LINEAGE_SQL || defaultSql;

/** Розбір одного рядка VALUES (...), поля в лапках '...' або NULL */
function parseMysqlValues(inner) {
  const parts = [];
  let i = 0;
  while (i < inner.length) {
    const c = inner[i];
    if (c === ',' || c === ' ') {
      i++;
      continue;
    }
    if (c === "'") {
      i++;
      let val = '';
      while (i < inner.length) {
        if (inner[i] === "'" && inner[i + 1] === "'") {
          val += "'";
          i += 2;
          continue;
        }
        if (inner[i] === "'") {
          i++;
          break;
        }
        val += inner[i++];
      }
      parts.push(val);
      continue;
    }
    if (inner.slice(i, i + 4) === 'NULL') {
      parts.push(null);
      i += 4;
      continue;
    }
    let j = i;
    while (j < inner.length && inner[j] !== ',') j++;
    parts.push(inner.slice(i, j).trim());
    i = j;
  }
  return parts;
}

async function main() {
  const map = new Map();
  const rl = createInterface({
    input: createReadStream(sqlPath, { encoding: 'utf8' }),
    crlfDelay: Infinity,
  });

  for await (const line of rl) {
    if (!line.startsWith('INSERT INTO `npc` VALUES')) continue;
    const open = line.indexOf('VALUES (');
    if (open < 0) continue;
    const close = line.lastIndexOf(');');
    if (close < open) continue;
    const inner = line.slice(open + 'VALUES ('.length, close);
    const parts = parseMysqlValues(inner);
    if (parts.length < 25) continue;
    const npcid = parseInt(String(parts[1]), 10);
    /** У дампі npc: після men йдуть exp (23), sp (24), patk (25) — індекси з 0. */
    const exp = parseInt(String(parts[23]), 10);
    const sp = parseInt(String(parts[24]), 10);
    if (!Number.isFinite(npcid)) continue;
    if (!Number.isFinite(exp)) continue;
    if (!Number.isFinite(sp)) continue;
    map.set(npcid, { exp, sp });
  }

  const keys = [...map.keys()].sort((a, b) => a - b);
  const lines = keys.map(
    (k) => `  ${k}: { exp: ${map.get(k).exp}, sp: ${map.get(k).sp} }`
  );

  const header = `// AUTO-GENERATED by server/scripts/gen-l2dop-npc-exp-sp.mjs — do not edit by hand.
// Джерело: l2dop/lineage.sql (таблиця npc, поля exp, sp).

export const L2DOP_NPC_EXP_SP: Record<number, { exp: number; sp: number }> = {
`;

  const out = createWriteStream(outPath, { encoding: 'utf8' });
  out.write(header);
  out.write(lines.join(',\n'));
  out.write('\n};\n');
  out.end();
  await new Promise((res, rej) => {
    out.on('finish', res);
    out.on('error', rej);
  });
  console.log('Wrote', outPath, 'entries:', keys.length);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
