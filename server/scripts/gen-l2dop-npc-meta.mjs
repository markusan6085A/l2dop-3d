/**
 * Читає lineage.sql (l2dop) — INSERT INTO `npc` — рівень (level) і type.
 * Запуск: node server/scripts/gen-l2dop-npc-meta.mjs
 */
import { createReadStream, createWriteStream } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';
import { createInterface } from 'readline';

const __dirname = dirname(fileURLToPath(import.meta.url));
const serverDir = join(__dirname, '..');
const defaultSql = join(serverDir, '..', '..', 'l2dop', 'lineage.sql');
const outPath = join(serverDir, 'src', 'data', 'l2dopNpcMeta.generated.ts');
const sqlPath = process.env.L2DOP_LINEAGE_SQL || defaultSql;

function parseMysqlValues(inner) {
  const parts = [];
  let i = 0;
  while (i < inner.length) {
    const c = inner[i];
    if (c === ',' || c === ' ') {
      i++;
      continue;
    }
    if (c === "'") {
      i++;
      let val = '';
      while (i < inner.length) {
        if (inner[i] === "'" && inner[i + 1] === "'") {
          val += "'";
          i += 2;
          continue;
        }
        if (inner[i] === "'") {
          i++;
          break;
        }
        val += inner[i++];
      }
      parts.push(val);
      continue;
    }
    if (inner.slice(i, i + 4) === 'NULL') {
      parts.push(null);
      i += 4;
      continue;
    }
    let j = i;
    while (j < inner.length && inner[j] !== ',') j++;
    parts.push(inner.slice(i, j).trim());
    i = j;
  }
  return parts;
}

async function main() {
  const levelMap = new Map();
  const typeMap = new Map();

  const rl = createInterface({
    input: createReadStream(sqlPath, { encoding: 'utf8' }),
    crlfDelay: Infinity,
  });

  for await (const line of rl) {
    if (!line.startsWith('INSERT INTO `npc` VALUES')) continue;
    const open = line.indexOf('VALUES (');
    if (open < 0) continue;
    const close = line.lastIndexOf(');');
    if (close < open) continue;
    const inner = line.slice(open + 'VALUES ('.length, close);
    const parts = parseMysqlValues(inner);
    if (parts.length < 12) continue;
    const npcid = parseInt(String(parts[1]), 10);
    const level = parseInt(String(parts[9]), 10);
    const typ = parts[11] != null ? String(parts[11]) : '';
    if (!Number.isFinite(npcid)) continue;
    if (Number.isFinite(level) && level >= 0) levelMap.set(npcid, level);
    if (typ) typeMap.set(npcid, typ);
  }

  const npcIds = [...new Set([...levelMap.keys(), ...typeMap.keys()])].sort(
    (a, b) => a - b
  );

  const levelLines = npcIds.map(
    (id) =>
      `  ${id}: ${levelMap.has(id) ? levelMap.get(id) : 1}`
  );
  const typeLines = npcIds.map((id) => {
    const t = typeMap.get(id) ?? '';
    return `  ${id}: ${JSON.stringify(t)}`;
  });

  const out = createWriteStream(outPath, { encoding: 'utf8' });
  out.write(`// AUTO-GENERATED by server/scripts/gen-l2dop-npc-meta.mjs — do not edit by hand.
// Джерело: l2dop/lineage.sql (таблиця npc: level, type).

/** Рівень NPC з дампу (для підбору дропу за схожим рівнем). */
export const L2DOP_NPC_LEVEL: Record<number, number> = {
`);
  out.write(levelLines.join(',\n'));
  out.write(`
};

/** Тип NPC (L2Monster, L2RaidBoss, …) — для відсікання не-мобів у fallback дропу. */
export const L2DOP_NPC_TYPE: Record<number, string> = {
`);
  out.write(typeLines.join(',\n'));
  out.write(`
};
`);
  out.end();

  await new Promise((resolve, reject) => {
    out.on('finish', resolve);
    out.on('error', reject);
  });

  console.log(`OK: ${npcIds.length} NPC → ${outPath}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
