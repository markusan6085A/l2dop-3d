/**
 * З l2dop/lineage.sql (таблиця npc) — ім'я → npcid.
 * При дублікатах імен обираємо запис з вищим пріоритетом типу (L2Monster > L2Minion > L2Pet).
 */
import { createReadStream } from 'fs';
import { createWriteStream } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';
import { createInterface } from 'readline';

const __dirname = dirname(fileURLToPath(import.meta.url));
const serverDir = join(__dirname, '..');
const defaultSql = join(serverDir, '..', '..', 'l2dop', 'lineage.sql');
const outPath = join(serverDir, 'src', 'data', 'l2dopNpcIdByLineageName.generated.ts');

const sqlPath = process.env.L2DOP_LINEAGE_SQL || defaultSql;

function parseMysqlValues(inner) {
  const parts = [];
  let i = 0;
  while (i < inner.length) {
    const c = inner[i];
    if (c === ',' || c === ' ') {
      i++;
      continue;
    }
    if (c === "'") {
      i++;
      let val = '';
      while (i < inner.length) {
        if (inner[i] === "'" && inner[i + 1] === "'") {
          val += "'";
          i += 2;
          continue;
        }
        if (inner[i] === "'") {
          i++;
          break;
        }
        val += inner[i++];
      }
      parts.push(val);
      continue;
    }
    if (inner.slice(i, i + 4) === 'NULL') {
      parts.push(null);
      i += 4;
      continue;
    }
    let j = i;
    while (j < inner.length && inner[j] !== ',') j++;
    parts.push(inner.slice(i, j).trim());
    i = j;
  }
  return parts;
}

/** Чим більше — тим краще для дропу/бою */
function typePriority(t) {
  if (!t || typeof t !== 'string') return 0;
  if (t === 'L2GrandBoss') return 400;
  if (t === 'L2RaidBoss') return 350;
  if (t === 'L2Monster') return 300;
  if (t === 'L2Minion') return 200;
  if (t === 'L2Npc') return 50;
  if (t === 'L2Pet') return 25;
  return 100;
}

async function main() {
  /** name -> { npcid, pri } */
  const best = new Map();

  const rl = createInterface({
    input: createReadStream(sqlPath, { encoding: 'utf8' }),
    crlfDelay: Infinity,
  });

  for await (const line of rl) {
    if (!line.startsWith('INSERT INTO `npc` VALUES')) continue;
    const open = line.indexOf('VALUES (');
    if (open < 0) continue;
    const close = line.lastIndexOf(');');
    if (close < open) continue;
    const inner = line.slice(open + 'VALUES ('.length, close);
    const parts = parseMysqlValues(inner);
    if (parts.length < 12) continue;
    const npcid = parseInt(String(parts[1]), 10);
    const name = parts[2];
    const type = parts[11];
    if (!Number.isFinite(npcid) || name == null || typeof name !== 'string') continue;
    const n = name.trim();
    if (!n.length) continue;
    const pri = typePriority(type);
    const prev = best.get(n);
    if (!prev || pri > prev.pri || (pri === prev.pri && npcid > prev.npcid)) {
      best.set(n, { npcid, pri });
    }
  }

  const keys = [...best.keys()].sort((a, b) => a.localeCompare(b));
  const lines = keys.map((k) => {
    const id = best.get(k).npcid;
    return `  ${JSON.stringify(k)}: ${id}`;
  });

  const header = `// AUTO-GENERATED by server/scripts/gen-l2dop-npc-id-by-lineage-name.mjs — do not edit by hand.
// Джерело: l2dop/lineage.sql (npc.name → npcid; пріоритет L2Monster тощо).

export const L2DOP_NPC_ID_BY_LINEAGE_NAME: Record<string, number> = {
`;

  const out = createWriteStream(outPath, { encoding: 'utf8' });
  out.write(header);
  out.write(lines.join(',\n'));
  out.write('\n};\n');
  out.end();
  await new Promise((res, rej) => {
    out.on('finish', res);
    out.on('error', rej);
  });
  console.log('Wrote', outPath, 'entries:', keys.length);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
